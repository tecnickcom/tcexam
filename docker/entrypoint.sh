#!/bin/sh
#============================================================+
# File name   : entrypoint.sh
# Begin       : 2026-06-22
#
# Description : Container entrypoint for the TCExam runtime image.
#               Runs the non-interactive installer (idempotent) so the stack
#               is ready without a browser install step, generates the
#               install-time build artifacts (PDF fonts and translation
#               caches) on first run, fixes runtime permissions, then executes
#               the container command (Apache).
#
